class EnhancedPPOAgent:
    """
    Улучшенный PPO с несколькими эпохами обновления, энтропийным бонусом,
    cosine annealing и AdamW. Корректно использует пошаговые награды и done флаги.
    """
    def __init__(self, n_warehouses, learning_rate=0.0003, gamma=0.99, clip_epsilon=0.1, entropy_coef=0.02):
        self.actor_critic = EnhancedActorCriticNetwork(n_warehouses)
        self.optimizer = optim.AdamW(self.actor_critic.parameters(), lr=learning_rate, weight_decay=1e-4)
        self.scheduler = optim.lr_scheduler.CosineAnnealingLR(self.optimizer, T_max=1500)
        self.gamma = gamma
        self.clip_epsilon = clip_epsilon
        self.entropy_coef = entropy_coef
        self.memory = []   # хранит переходы эпизода

    def select_action(self, state):
        """Выбирает действие, сохраняет переход с пустыми reward/done (заполнятся позже)."""
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        with torch.no_grad():
            probs, state_value = self.actor_critic(state_tensor)
            m = dist.Categorical(probs)
            action = m.sample()
        self.memory.append({
            'state': state_tensor,
            'action': action,
            'log_prob': m.log_prob(action),
            'value': state_value,
            'entropy': m.entropy(),
            'reward': 0.0,
            'done': False
        })
        return action.item()

    def set_last_reward(self, reward, done):
        """Заполняет reward и done для последнего сохранённого перехода."""
        if self.memory:
            self.memory[-1]['reward'] = reward
            self.memory[-1]['done'] = done

    def update(self):
        """
        Вычисляет returns с учётом done, преимущества, затем выполняет
        несколько эпох PPO‑обновления (clipped loss, value loss, энтропийный бонус).
        """
        if not self.memory:
            return
        rewards = [m['reward'] for m in self.memory]
        dones = [m['done'] for m in self.memory]
        states = [m['state'] for m in self.memory]
        actions = torch.stack([m['action'] for m in self.memory])
        old_log_probs = torch.stack([m['log_prob'] for m in self.memory])
        old_values = torch.cat([m['value'] for m in self.memory])
        old_entropies = torch.stack([m['entropy'] for m in self.memory])

        # вычисляем returns с учётом done (обнуляем R после done)
        returns = []
        R = 0
        for r, d in zip(reversed(rewards), reversed(dones)):
            if d:
                R = 0
            R = r + self.gamma * R
            returns.insert(0, R)
        returns = torch.tensor(returns, dtype=torch.float32)
        advantages = returns - old_values.squeeze()
        advantages = (advantages - advantages.mean()) / (advantages.std() + 1e-8)

        # несколько эпох обучения на одном эпизоде
        for _ in range(4):
            new_log_probs = []
            new_values = []
            new_entropies = []
            for i, state in enumerate(states):
                probs, value = self.actor_critic(state)
                d = dist.Categorical(probs)
                new_log_probs.append(d.log_prob(actions[i]))
                new_values.append(value)
                new_entropies.append(d.entropy())
            new_log_probs = torch.stack(new_log_probs)
            new_values = torch.cat(new_values)
            new_entropies = torch.stack(new_entropies)
            ratio = (new_log_probs - old_log_probs).exp()
            surr1 = ratio * advantages
            surr2 = torch.clamp(ratio, 1 - self.clip_epsilon, 1 + self.clip_epsilon) * advantages
            policy_loss = -torch.min(surr1, surr2).mean()
            value_loss = nn.MSELoss()(new_values.squeeze(), returns)
            entropy_bonus = -self.entropy_coef * new_entropies.mean()
            loss = policy_loss + 0.5 * value_loss + entropy_bonus
            self.optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(self.actor_critic.parameters(), max_norm=1.0)
            self.optimizer.step()
        self.scheduler.step()
        self.memory = []


class ImprovedDQNAgent:
    """
    Улучшенный DQN с Duelling архитектурой, Prioritized Experience Replay,
    мягким обновлением целевой сети, AdamW, cosine annealing,
    опционально Noisy сетями вместо ε‑greedy.
    """
    def __init__(self, n_warehouses, learning_rate=0.0003, gamma=0.99, tau=0.005, use_per=True, use_noisy=False):
        self.use_noisy = use_noisy
        if use_noisy:
            self.q_network = NoisyDuelingQNetwork(n_warehouses)
            self.target_network = NoisyDuelingQNetwork(n_warehouses)
        else:
            self.q_network = DuelingQNetwork(n_warehouses)
            self.target_network = DuelingQNetwork(n_warehouses)
        self.target_network.load_state_dict(self.q_network.state_dict())
        self.optimizer = optim.AdamW(self.q_network.parameters(), lr=learning_rate, weight_decay=1e-4)
        self.scheduler = optim.lr_scheduler.CosineAnnealingLR(self.optimizer, T_max=1500)
        self.gamma = gamma
        self.tau = tau          # коэффициент мягкого обновления целевой сети
        self.use_per = use_per
        if use_per:
            self.memory = PrioritizedReplayBuffer(capacity=10000, alpha=0.6)
            self.beta = 0.4
            self.beta_increment = 0.001
        else:
            self.memory = deque(maxlen=10000)
        self.batch_size = 128
        if not use_noisy:
            self.epsilon = 1.0
            self.epsilon_decay = 0.995
            self.epsilon_min = 0.01
        else:
            self.epsilon = 0.0   # не нужен при Noisy

    def select_action(self, state):
        """Выбор действия: ε‑greedy или argmax по Noisy сети."""
        if not self.use_noisy and random.random() < self.epsilon:
            visited = state['visited_mask']
            valid = [i for i, v in enumerate(visited) if v == 0]
            return random.choice(valid) if valid else 0
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        with torch.no_grad():
            q_values = self.q_network(state_tensor)
            return q_values.argmax().item()

    def store_experience(self, state, action, reward, next_state, done):
        """Добавляет переход в PER‑буфер или обычный deque."""
        experience = (state, action, reward, next_state, done)
        if self.use_per:
            priority = self.memory.max_priority()
            self.memory.add(experience, priority)
        else:
            self.memory.append(experience)

    def update(self):
        """Обучает Q‑сеть на батче с возможной коррекцией по важности (PER)."""
        if len(self.memory) < self.batch_size:
            return
        if self.use_per:
            batch, indices, weights = self.memory.sample(self.batch_size, self.beta)
            if len(batch) < self.batch_size:
                return
            self.beta = min(1.0, self.beta + self.beta_increment)
            weights = torch.tensor(weights, dtype=torch.float32)
        else:
            batch = random.sample(list(self.memory), min(self.batch_size, len(self.memory)))
            weights = torch.ones(len(batch))
        states, actions, rewards, next_states, dones = zip(*batch)
        # Извлекаем компоненты состояния (учитываем dist_vector)
        state_current = torch.tensor([s['current'] for s in states], dtype=torch.long)
        state_visited = torch.tensor([s['visited_mask'] for s in states], dtype=torch.float32)
        state_distvec = torch.tensor([s['dist_vector'] for s in states], dtype=torch.float32)
        next_current = torch.tensor([s['current'] for s in next_states], dtype=torch.long)
        next_visited = torch.tensor([s['visited_mask'] for s in next_states], dtype=torch.float32)
        next_distvec = torch.tensor([s['dist_vector'] for s in next_states], dtype=torch.float32)
        state_tensors = {'current': state_current, 'visited_mask': state_visited, 'dist_vector': state_distvec}
        next_tensors = {'current': next_current, 'visited_mask': next_visited, 'dist_vector': next_distvec}
        actions = torch.tensor(actions)
        rewards = torch.tensor(rewards, dtype=torch.float32)
        dones = torch.tensor(dones, dtype=torch.float32)

        # Double DQN: выбор действия по основной сети, оценка по целевой
        with torch.no_grad():
            next_actions = self.q_network(next_tensors).argmax(1)
            next_q = self.target_network(next_tensors)[torch.arange(len(batch)), next_actions]
            targets = rewards + self.gamma * next_q * (1 - dones)
        current_q = self.q_network(state_tensors)[torch.arange(len(batch)), actions]
        td_errors = targets - current_q
        loss = (weights * td_errors.pow(2)).mean()
        self.optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(self.q_network.parameters(), max_norm=1.0)
        self.optimizer.step()
        self.scheduler.step()

        # мягкое обновление целевой сети
        for target_param, param in zip(self.target_network.parameters(), self.q_network.parameters()):
            target_param.data.copy_(self.tau * param.data + (1 - self.tau) * target_param.data)

        if self.use_per:
            self.memory.update_priorities(indices, td_errors.detach().abs().numpy())
        if self.use_noisy and hasattr(self.q_network, 'reset_noise'):
            self.q_network.reset_noise()
            self.target_network.reset_noise()
        if not self.use_noisy:
            self.epsilon = max(self.epsilon_min, self.epsilon * self.epsilon_decay)


class ImprovedDoubleDQNAgent(ImprovedDQNAgent):
    """
    Улучшенный Double DQN с увеличенным батчем, мягким обновлением таргет-сети
    и периодическим жёстким копированием весов для стабильности.
    """
    def __init__(self, n_warehouses, learning_rate=0.0003, gamma=0.99, tau=0.001, use_per=True, use_noisy=False):
        super().__init__(n_warehouses, learning_rate, gamma, tau, use_per, use_noisy)
        self.batch_size = 256
        self.tau = tau
        self.update_step = 0

    def update(self):
        """Выполняет обновление DQN и раз в 1000 шагов жёстко копирует веса."""
        super().update()
        self.update_step += 1
        if self.update_step % 1000 == 0:
            self.target_network.load_state_dict(self.q_network.state_dict())


class EnhancedSACAgent:
    """
    Улучшенный Soft Actor‑Critic для дискретных действий:
    автоматическая настройка энтропийного коэффициента α,
    дуэльные Q‑сети, PER, AdamW, cosine annealing.
    """
    def __init__(self, n_warehouses, learning_rate=0.0003, gamma=0.99, tau=0.005, alpha=0.2, use_per=True):
        self.actor = EnhancedPolicyNetwork(n_warehouses)           # политика
        self.critic1 = EnhancedSACNetwork(n_warehouses)            # первый критик
        self.critic2 = EnhancedSACNetwork(n_warehouses)            # второй критик
        self.critic1_target = EnhancedSACNetwork(n_warehouses)     # целевые сети
        self.critic2_target = EnhancedSACNetwork(n_warehouses)
        self.critic1_target.load_state_dict(self.critic1.state_dict())
        self.critic2_target.load_state_dict(self.critic2.state_dict())
        self.actor_optimizer = optim.AdamW(self.actor.parameters(), lr=learning_rate, weight_decay=1e-4)
        self.critic1_optimizer = optim.AdamW(self.critic1.parameters(), lr=learning_rate, weight_decay=1e-4)
        self.critic2_optimizer = optim.AdamW(self.critic2.parameters(), lr=learning_rate, weight_decay=1e-4)
        self.actor_scheduler = optim.lr_scheduler.CosineAnnealingLR(self.actor_optimizer, T_max=1500)
        self.critic1_scheduler = optim.lr_scheduler.CosineAnnealingLR(self.critic1_optimizer, T_max=1500)
        self.critic2_scheduler = optim.lr_scheduler.CosineAnnealingLR(self.critic2_optimizer, T_max=1500)
        self.gamma = gamma
        self.tau = tau
        self.target_entropy = -np.log(1.0 / n_warehouses) * 0.98   # желаемый уровень энтропии
        # log α – обучаемый параметр температуры; инициализируем на CPU, при использовании перенесём на нужное устройство
        self.log_alpha = torch.tensor(np.log(alpha), requires_grad=True, dtype=torch.float32)
        self.alpha_optimizer = optim.AdamW([self.log_alpha], lr=learning_rate)
        self.use_per = use_per
        if use_per:
            self.memory = PrioritizedReplayBuffer(capacity=10000, alpha=0.6)
            self.beta = 0.4
            self.beta_increment = 0.001
        else:
            self.memory = deque(maxlen=10000)
        self.batch_size = 256

    def select_action(self, state):
        """Сэмплирует действие из политики."""
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        probs = self.actor(state_tensor)
        m = dist.Categorical(probs)
        action = m.sample()
        return action.item()

    def store_experience(self, state, action, reward, next_state, done):
        """Сохраняет переход с приоритетом."""
        experience = (state, action, reward, next_state, done)
        if self.use_per:
            priority = self.memory.max_priority()
            self.memory.add(experience, priority)
        else:
            self.memory.append(experience)

    def _soft_update(self, target, source):
        """Мягкое обновление целевых сетей."""
        for target_param, param in zip(target.parameters(), source.parameters()):
            target_param.data.copy_(self.tau * param.data + (1 - self.tau) * target_param.data)

    def update(self):
        """Обучает критиков и актора с автоматической настройкой α."""
        if len(self.memory) < self.batch_size:
            return
        if self.use_per:
            batch, indices, weights = self.memory.sample(self.batch_size, self.beta)
            if len(batch) < self.batch_size:
                return
            self.beta = min(1.0, self.beta + self.beta_increment)
            weights = torch.tensor(weights, dtype=torch.float32)
        else:
            batch = random.sample(list(self.memory), min(self.batch_size, len(self.memory)))
            weights = torch.ones(len(batch))
        states, actions, rewards, next_states, dones = zip(*batch)
        # Преобразуем состояния с учётом dist_vector
        state_current = torch.tensor([s['current'] for s in states], dtype=torch.long)
        state_visited = torch.tensor([s['visited_mask'] for s in states], dtype=torch.float32)
        state_distvec = torch.tensor([s['dist_vector'] for s in states], dtype=torch.float32)
        next_current = torch.tensor([s['current'] for s in next_states], dtype=torch.long)
        next_visited = torch.tensor([s['visited_mask'] for s in next_states], dtype=torch.float32)
        next_distvec = torch.tensor([s['dist_vector'] for s in next_states], dtype=torch.float32)
        state_tensors = {'current': state_current, 'visited_mask': state_visited, 'dist_vector': state_distvec}
        next_tensors = {'current': next_current, 'visited_mask': next_visited, 'dist_vector': next_distvec}
        actions = torch.tensor(actions)
        rewards = torch.tensor(rewards, dtype=torch.float32)
        dones = torch.tensor(dones, dtype=torch.float32)

        # Переносим log_alpha на нужное устройство, если ещё не там
        if self.log_alpha.device != state_current.device:
            self.log_alpha = self.log_alpha.to(state_current.device)
        alpha = self.log_alpha.exp()

        # Вычисляем target для критика
        with torch.no_grad():
            next_probs = self.actor(next_tensors)
            next_log_probs = torch.log(next_probs + 1e-8)
            next_q1 = self.critic1_target(next_tensors)
            next_q2 = self.critic2_target(next_tensors)
            next_q = torch.min(next_q1, next_q2)
            next_value = (next_probs * (next_q - alpha * next_log_probs)).sum(dim=1)
            targets = rewards + self.gamma * next_value * (1 - dones)

        # Лосс критиков
        current_q1 = self.critic1(state_tensors)[torch.arange(len(batch)), actions]
        current_q2 = self.critic2(state_tensors)[torch.arange(len(batch)), actions]
        critic1_loss = (weights * (current_q1 - targets).pow(2)).mean()
        critic2_loss = (weights * (current_q2 - targets).pow(2)).mean()
        self.critic1_optimizer.zero_grad()
        critic1_loss.backward()
        torch.nn.utils.clip_grad_norm_(self.critic1.parameters(), max_norm=1.0)
        self.critic1_optimizer.step()
        self.critic2_optimizer.zero_grad()
        critic2_loss.backward()
        torch.nn.utils.clip_grad_norm_(self.critic2.parameters(), max_norm=1.0)
        self.critic2_optimizer.step()

        # Лосс актора
        probs = self.actor(state_tensors)
        log_probs = torch.log(probs + 1e-8)
        with torch.no_grad():
            q1 = self.critic1(state_tensors)
            q2 = self.critic2(state_tensors)
            q = torch.min(q1, q2)
        actor_loss = (probs * (alpha.detach() * log_probs - q)).sum(dim=1).mean()
        self.actor_optimizer.zero_grad()
        actor_loss.backward()
        torch.nn.utils.clip_grad_norm_(self.actor.parameters(), max_norm=1.0)
        self.actor_optimizer.step()

        # Автоматическая настройка α
        alpha_loss = -(self.log_alpha * (log_probs.detach().mean() + self.target_entropy)).mean()
        self.alpha_optimizer.zero_grad()
        alpha_loss.backward()
        self.alpha_optimizer.step()

        # Мягкое обновление целевых сетей
        self._soft_update(self.critic1_target, self.critic1)
        self._soft_update(self.critic2_target, self.critic2)

        self.actor_scheduler.step()
        self.critic1_scheduler.step()
        self.critic2_scheduler.step()

        if self.use_per:
            td_errors = torch.abs(targets - current_q1).detach().numpy()
            self.memory.update_priorities(indices, td_errors)