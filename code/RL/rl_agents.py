class PolicyGradientAgent:
    """
    Агент на основе метода Policy Gradient (REINFORCE) с базовым уровнем.
    Собирает логарифмы вероятностей и награды за эпизод, затем обновляет политику
    с помощью градиентного подъёма по ожидаемому дисконтированному доходу.
    """
    def __init__(self, n_warehouses, learning_rate=0.001):
        self.policy_network = PolicyNetwork(n_warehouses)          # Actor
        self.optimizer = optim.Adam(self.policy_network.parameters(), lr=learning_rate)
        self.saved_log_probs = []   # логарифмы вероятностей выбранных действий
        self.rewards = []           # пошаговые награды

    def select_action(self, state):
        """
        Выбирает действие сэмплированием из распределения политики.
        Сохраняет логарифм вероятности для последующего обучения.
        """
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        probs = self.policy_network(state_tensor)                  # (1, n_nodes)
        m = dist.Categorical(probs)
        action = m.sample()
        self.saved_log_probs.append(m.log_prob(action))
        return action.item()

    def update(self):
        """
        Вычисляет дисконтированные возвраты, нормализует их и применяет
        градиентный шаг REINFORCE: loss = -log_prob * return.
        """
        if not self.rewards:
            return
        R = 0
        returns = []
        for r in reversed(self.rewards):
            R = r + 0.99 * R
            returns.insert(0, R)
        returns = torch.tensor(returns)
        returns = (returns - returns.mean()) / (returns.std() + 1e-9)
        policy_loss = []
        for log_prob, R in zip(self.saved_log_probs, returns):
            policy_loss.append(-log_prob * R)
        self.optimizer.zero_grad()
        loss = torch.stack(policy_loss).sum()
        loss.backward()
        self.optimizer.step()
        self.saved_log_probs = []
        self.rewards = []


class A2CAgent:
    """
    Advantage Actor‑Critic (A2C). Использует actor‑critic архитектуру,
    где critic оценивает функцию ценности V(s), а actor обновляется
    с использованием преимущества A(s,a) = R - V(s).
    """
    def __init__(self, n_warehouses, learning_rate=0.001, gamma=0.99):
        self.actor_critic = ActorCriticNetwork(n_warehouses)
        self.optimizer = optim.Adam(self.actor_critic.parameters(), lr=learning_rate)
        self.gamma = gamma
        self.saved_log_probs = []   # log_prob действий
        self.rewards = []           # пошаговые награды
        self.state_values = []      # оценки V(s) от критика

    def select_action(self, state):
        """Выбирает действие, сохраняет log_prob и V(s)."""
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        probs, state_value = self.actor_critic(state_tensor)
        m = dist.Categorical(probs)
        action = m.sample()
        self.saved_log_probs.append(m.log_prob(action))
        self.state_values.append(state_value)
        return action.item()

    def update(self):
        """
        Вычисляет преимущества, суммирует лоссы политики и ценности,
        выполняет градиентный шаг.
        """
        if not self.rewards:
            return
        R = 0
        returns = []
        for r in reversed(self.rewards):
            R = r + self.gamma * R
            returns.insert(0, R)
        returns = torch.tensor(returns)
        returns = (returns - returns.mean()) / (returns.std() + 1e-9)
        state_values = torch.cat(self.state_values)
        advantages = returns - state_values.squeeze()
        policy_loss = []
        for log_prob, adv in zip(self.saved_log_probs, advantages):
            policy_loss.append(-log_prob * adv.detach())
        value_loss = nn.MSELoss()(state_values.squeeze(), returns)
        self.optimizer.zero_grad()
        loss = torch.stack(policy_loss).sum() + value_loss
        loss.backward()
        self.optimizer.step()
        self.saved_log_probs = []
        self.rewards = []
        self.state_values = []


class PPOAgent:
    """
    Proximal Policy Optimization (PPO) с корректным распределением наград по шагам.
    Сохраняет пошаговые вознаграждения и флаги завершения, затем вычисляет
    дисконтированные возвраты с учётом done. Используется несколько эпох обновления
    для стабильности.
    """
    def __init__(self, n_warehouses, learning_rate=0.001, gamma=0.99, clip_epsilon=0.2):
        self.actor_critic = ActorCriticNetwork(n_warehouses)
        self.optimizer = optim.Adam(self.actor_critic.parameters(), lr=learning_rate)
        self.gamma = gamma
        self.clip_epsilon = clip_epsilon
        self.memory = []   # хранит переходы для всего эпизода

    def select_action(self, state):
        """Выбирает действие, сохраняет переход с пустой наградой (будет заполнена позже)."""
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        with torch.no_grad():
            probs, state_value = self.actor_critic(state_tensor)
            m = dist.Categorical(probs)
            action = m.sample()
        self.memory.append({
            'state': state_tensor,
            'action': action,
            'log_prob': m.log_prob(action),
            'value': state_value,
            'reward': 0.0,      # будет заполнено позже
            'done': False
        })
        return action.item()

    def set_last_reward(self, reward, done):
        """
        Вызывается после env.step для последнего добавленного перехода.
        Заполняет reward и done.
        """
        if self.memory:
            self.memory[-1]['reward'] = reward
            self.memory[-1]['done'] = done

    def update(self):
        """
        Вычисляет returns и преимущества, затем выполняет несколько эпох
        PPO‑обновления (clipped surrogate objective + value loss).
        """
        if not self.memory:
            return
        rewards = [m['reward'] for m in self.memory]
        dones = [m['done'] for m in self.memory]
        states = [m['state'] for m in self.memory]
        actions = torch.stack([m['action'] for m in self.memory])
        old_log_probs = torch.stack([m['log_prob'] for m in self.memory])
        old_values = torch.cat([m['value'] for m in self.memory])

        # вычисляем returns с учётом done
        returns = []
        R = 0
        for r, d in zip(reversed(rewards), reversed(dones)):
            if d:
                R = 0                   # сброс на границе эпизода (если done=True)
            R = r + self.gamma * R
            returns.insert(0, R)
        returns = torch.tensor(returns, dtype=torch.float32)
        advantages = returns - old_values.squeeze()
        advantages = (advantages - advantages.mean()) / (advantages.std() + 1e-8)

        # Несколько эпох обучения на одном и том же эпизоде
        for _ in range(3):
            new_log_probs = []
            new_values = []
            for i, state in enumerate(states):
                probs, value = self.actor_critic(state)
                dist_ = dist.Categorical(probs)
                new_log_probs.append(dist_.log_prob(actions[i]))
                new_values.append(value)
            new_log_probs = torch.stack(new_log_probs)
            new_values = torch.cat(new_values)
            ratio = (new_log_probs - old_log_probs).exp()
            surr1 = ratio * advantages
            surr2 = torch.clamp(ratio, 1 - self.clip_epsilon, 1 + self.clip_epsilon) * advantages
            policy_loss = -torch.min(surr1, surr2).mean()
            value_loss = nn.MSELoss()(new_values.squeeze(), returns)
            loss = policy_loss + 0.5 * value_loss
            self.optimizer.zero_grad()
            loss.backward()
            self.optimizer.step()
        self.memory = []


class DQNAgent:
    """
    Deep Q‑Network (DQN). Аппроксимирует Q‑функцию, использует целевую сеть
    и experience replay. Выбор действия – ε‑greedy.
    """
    def __init__(self, n_warehouses, learning_rate=0.001, gamma=0.99, epsilon=1.0, epsilon_decay=0.995):
        self.q_network = QNetwork(n_warehouses)
        self.target_network = QNetwork(n_warehouses)
        self.target_network.load_state_dict(self.q_network.state_dict())
        self.optimizer = optim.Adam(self.q_network.parameters(), lr=learning_rate)
        self.gamma = gamma
        self.epsilon = epsilon
        self.epsilon_decay = epsilon_decay
        self.memory = deque(maxlen=10000)
        self.batch_size = 32

    def select_action(self, state):
        """ε‑greedy выбор действия."""
        if random.random() < self.epsilon:
            visited = state['visited_mask']
            valid = [i for i, v in enumerate(visited) if v == 0]
            return random.choice(valid) if valid else 0
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        with torch.no_grad():
            q_values = self.q_network(state_tensor)
            return q_values.argmax().item()

    def store_experience(self, state, action, reward, next_state, done):
        """Сохраняет переход в replay-буфер."""
        self.memory.append((state, action, reward, next_state, done))

    def update(self):
        """Один шаг обучения по мини-батчу из replay-буфера."""
        if len(self.memory) < self.batch_size:
            return
        batch = random.sample(self.memory, self.batch_size)
        states, actions, rewards, next_states, dones = zip(*batch)
        state_current = torch.tensor([s['current'] for s in states], dtype=torch.long)
        state_visited = torch.tensor([s['visited_mask'] for s in states], dtype=torch.float32)
        state_distvec = torch.tensor([s['dist_vector'] for s in states], dtype=torch.float32)
        next_current = torch.tensor([s['current'] for s in next_states], dtype=torch.long)
        next_visited = torch.tensor([s['visited_mask'] for s in next_states], dtype=torch.float32)
        next_distvec = torch.tensor([s['dist_vector'] for s in next_states], dtype=torch.float32)
        state_tensors = {'current': state_current, 'visited_mask': state_visited, 'dist_vector': state_distvec}
        next_tensors = {'current': next_current, 'visited_mask': next_visited, 'dist_vector': next_distvec}
        actions = torch.tensor(actions)
        rewards = torch.tensor(rewards, dtype=torch.float32)
        dones = torch.tensor(dones, dtype=torch.float32)
        with torch.no_grad():
            next_q = self.target_network(next_tensors).max(1)[0]
            targets = rewards + self.gamma * next_q * (1 - dones)
        current_q = self.q_network(state_tensors)[torch.arange(self.batch_size), actions]
        loss = nn.MSELoss()(current_q, targets)
        self.optimizer.zero_grad()
        loss.backward()
        self.optimizer.step()
        self.epsilon = max(0.01, self.epsilon * self.epsilon_decay)
        if random.random() < 0.01:
            self.target_network.load_state_dict(self.q_network.state_dict())


class DoubleDQNAgent(DQNAgent):
    """
    Double DQN. Разделяет выбор действия и оценку Q‑значения,
    чтобы уменьшить завышение оценок.
    """
    def update(self):
        if len(self.memory) < self.batch_size:
            return
        batch = random.sample(self.memory, self.batch_size)
        states, actions, rewards, next_states, dones = zip(*batch)
        state_current = torch.tensor([s['current'] for s in states], dtype=torch.long)
        state_visited = torch.tensor([s['visited_mask'] for s in states], dtype=torch.float32)
        state_distvec = torch.tensor([s['dist_vector'] for s in states], dtype=torch.float32)
        next_current = torch.tensor([s['current'] for s in next_states], dtype=torch.long)
        next_visited = torch.tensor([s['visited_mask'] for s in next_states], dtype=torch.float32)
        next_distvec = torch.tensor([s['dist_vector'] for s in next_states], dtype=torch.float32)
        state_tensors = {'current': state_current, 'visited_mask': state_visited, 'dist_vector': state_distvec}
        next_tensors = {'current': next_current, 'visited_mask': next_visited, 'dist_vector': next_distvec}
        actions = torch.tensor(actions)
        rewards = torch.tensor(rewards, dtype=torch.float32)
        dones = torch.tensor(dones, dtype=torch.float32)
        with torch.no_grad():
            # Выбираем действие по основной сети
            next_actions = self.q_network(next_tensors).argmax(1)
            # Оцениваем его по целевой сети
            next_q = self.target_network(next_tensors)[torch.arange(self.batch_size), next_actions]
            targets = rewards + self.gamma * next_q * (1 - dones)
        current_q = self.q_network(state_tensors)[torch.arange(self.batch_size), actions]
        loss = nn.MSELoss()(current_q, targets)
        self.optimizer.zero_grad()
        loss.backward()
        self.optimizer.step()
        self.epsilon = max(0.01, self.epsilon * self.epsilon_decay)
        if random.random() < 0.01:
            self.target_network.load_state_dict(self.q_network.state_dict())


class SACAgent:
    """
    Soft Actor‑Critic (SAC) для дискретных действий.
    Максимизирует и награду, и энтропию политики.
    Использует два критика для уменьшения завышения Q‑значений.
    """
    def __init__(self, n_warehouses, learning_rate=0.001, gamma=0.99, alpha=0.2):
        self.actor = PolicyNetwork(n_warehouses)
        self.critic1 = QNetwork(n_warehouses)
        self.critic2 = QNetwork(n_warehouses)
        self.actor_optimizer = optim.Adam(self.actor.parameters(), lr=learning_rate)
        self.critic1_optimizer = optim.Adam(self.critic1.parameters(), lr=learning_rate)
        self.critic2_optimizer = optim.Adam(self.critic2.parameters(), lr=learning_rate)
        self.gamma = gamma
        self.alpha = alpha          # коэффициент энтропии
        self.memory = deque(maxlen=10000)
        self.batch_size = 32

    def select_action(self, state):
        """Сэмплирует действие из политики."""
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        with torch.no_grad():
            probs = self.actor(state_tensor)
            m = dist.Categorical(probs)
            action = m.sample()
        return action.item()

    def store_experience(self, state, action, reward, next_state, done):
        """Сохраняет переход в replay-буфер."""
        self.memory.append((state, action, reward, next_state, done))

    def update(self):
        """
        Обучает обоих критиков и актора с использованием энтропийного бонуса.
        """
        if len(self.memory) < self.batch_size:
            return
        batch = random.sample(self.memory, self.batch_size)
        states, actions, rewards, next_states, dones = zip(*batch)
        state_current = torch.tensor([s['current'] for s in states], dtype=torch.long)
        state_visited = torch.tensor([s['visited_mask'] for s in states], dtype=torch.float32)
        state_distvec = torch.tensor([s['dist_vector'] for s in states], dtype=torch.float32)
        next_current = torch.tensor([s['current'] for s in next_states], dtype=torch.long)
        next_visited = torch.tensor([s['visited_mask'] for s in next_states], dtype=torch.float32)
        next_distvec = torch.tensor([s['dist_vector'] for s in next_states], dtype=torch.float32)
        state_tensors = {'current': state_current, 'visited_mask': state_visited, 'dist_vector': state_distvec}
        next_tensors = {'current': next_current, 'visited_mask': next_visited, 'dist_vector': next_distvec}
        actions = torch.tensor(actions)
        rewards = torch.tensor(rewards, dtype=torch.float32)
        dones = torch.tensor(dones, dtype=torch.float32)
        with torch.no_grad():
            next_probs = self.actor(next_tensors)
            next_log_probs = torch.log(next_probs + 1e-8)
            next_q1 = self.critic1(next_tensors)
            next_q2 = self.critic2(next_tensors)
            next_q = torch.min(next_q1, next_q2)
            next_value = (next_probs * (next_q - self.alpha * next_log_probs)).sum(dim=1)
            targets = rewards + self.gamma * next_value * (1 - dones)
        current_q1 = self.critic1(state_tensors)[torch.arange(self.batch_size), actions]
        current_q2 = self.critic2(state_tensors)[torch.arange(self.batch_size), actions]
        critic1_loss = nn.MSELoss()(current_q1, targets)
        critic2_loss = nn.MSELoss()(current_q2, targets)
        self.critic1_optimizer.zero_grad()
        critic1_loss.backward()
        self.critic1_optimizer.step()
        self.critic2_optimizer.zero_grad()
        critic2_loss.backward()
        self.critic2_optimizer.step()
        probs = self.actor(state_tensors)
        log_probs = torch.log(probs + 1e-8)
        with torch.no_grad():
            q1 = self.critic1(state_tensors)
            q2 = self.critic2(state_tensors)
            q = torch.min(q1, q2)
        actor_loss = (probs * (self.alpha * log_probs - q)).sum(dim=1).mean()
        self.actor_optimizer.zero_grad()
        actor_loss.backward()
        self.actor_optimizer.step()

# Улучшенные агенты
# Все они используют улучшенные нейросетевые архитектуры (Enhanced*),
# современные техники: AdamW, cosine annealing, gradient clipping,
# энтропийную регуляризацию, Prioritized Experience Replay, Noisy сети.

class EnhancedPolicyGradientAgent:
    """
    Улучшенный Policy Gradient (REINFORCE) с энтропийной регуляризацией,
    косинусным отжигом learning rate и gradient clipping.
    """
    def __init__(self, n_warehouses, learning_rate=0.001, gamma=0.99, entropy_coef=0.01):
        self.policy_network = EnhancedPolicyNetwork(n_warehouses)  # улучшенная сеть политики
        self.optimizer = optim.AdamW(self.policy_network.parameters(), lr=learning_rate, weight_decay=1e-4)
        self.scheduler = optim.lr_scheduler.CosineAnnealingLR(self.optimizer, T_max=1500)
        self.gamma = gamma
        self.entropy_coef = entropy_coef
        self.saved_log_probs = []   # log_prob выбранных действий
        self.rewards = []           # пошаговые награды
        self.entropies = []         # энтропия распределения для бонуса

    def select_action(self, state):
        """Выбирает действие, сохраняет log_prob и энтропию."""
        state_tensor = {
            'current': torch.tensor([state['current']], dtype=torch.long),
            'visited_mask': torch.tensor([state['visited_mask']], dtype=torch.float32),
            'dist_vector': torch.tensor([state['dist_vector']], dtype=torch.float32)
        }
        probs = self.policy_network(state_tensor)
        m = dist.Categorical(probs)
        action = m.sample()
        self.saved_log_probs.append(m.log_prob(action))
        self.entropies.append(m.entropy())
        return action.item()

    def update(self):
        """
        Вычисляет дисконтированные возвраты, нормализует их и применяет
        градиентный шаг с энтропийным бонусом.
        """
        if not self.rewards:
            return
        R = 0
        returns = []
        for r in reversed(self.rewards):
            R = r + self.gamma * R
            returns.insert(0, R)
        returns = torch.tensor(returns)
        returns = (returns - returns.mean()) / (returns.std() + 1e-8)
        policy_loss = []
        for log_prob, R in zip(self.saved_log_probs, returns):
            policy_loss.append(-log_prob * R)
        entropy = torch.stack(self.entropies).sum()
        loss = torch.stack(policy_loss).sum() - self.entropy_coef * entropy
        self.optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(self.policy_network.parameters(), max_norm=1.0)
        self.optimizer.step()
        self.scheduler.step()
        self.saved_log_probs = []
        self.rewards = []
        self.entropies = []


