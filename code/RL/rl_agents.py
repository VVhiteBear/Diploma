class ActorCriticNetwork(nn.Module):
    """
    Объединённая сеть Actor‑Critic с общим энкодером.
    Возвращает вероятности действий (Actor) и оценку ценности состояния V(s) (Critic).
    Применяется в A2C и PPO.
    """
    def __init__(self, n_warehouses, hidden_size=128):
        super().__init__()
        self.n_warehouses = n_warehouses
        # Общий энкодер для извлечения признаков
        self.encoder = nn.Sequential(
            nn.Linear(n_warehouses * 3, hidden_size),
            nn.ReLU(),
            nn.Linear(hidden_size, hidden_size),
            nn.ReLU()
        )
        self.actor = nn.Linear(hidden_size, n_warehouses)   # логиты действий
        self.critic = nn.Linear(hidden_size, 1)             # оценка V(s)

    def forward(self, state):
        current = state['current']
        visited_mask = state['visited_mask']
        dist_vector = state['dist_vector']
        current_onehot = torch.zeros(current.shape[0], self.n_warehouses, device=current.device)
        current_onehot.scatter_(1, current.unsqueeze(1), 1.0)
        x = torch.cat([current_onehot, visited_mask, dist_vector], dim=1)
        features = self.encoder(x)
        logits = self.actor(features)
        # Маскируем невозможные действия
        logits[visited_mask.bool()] = -1e9
        action_probs = torch.softmax(logits, dim=-1)
        state_value = self.critic(features)
        return action_probs, state_value

