# Базовые нейросетевые компоненты
# Все сети принимают на вход словарь состояния, содержащий:
#   current      – индекс текущего города (int -> later one-hot)
#   visited_mask – бинарный вектор посещённых городов (1 = посещён)
#   dist_vector  – нормализованные расстояния от текущего города до всех
# Размер входа: 3 * n_warehouses (one-hot + visited_mask + dist_vector)

class PolicyNetwork(nn.Module):
    """
    Нейросеть политики (Actor) для дискретного пространства действий.
    Используется в алгоритмах Policy Gradient, A2C, PPO и SAC.
    Возвращает вероятности выбора каждого из оставшихся (непосещённых) городов.
    """
    def __init__(self, n_warehouses, hidden_size=128):
        super().__init__()
        self.n_warehouses = n_warehouses
        # Три компоненты входа: one_hot(current), visited_mask, dist_vector
        self.network = nn.Sequential(
            nn.Linear(n_warehouses * 3, hidden_size),
            nn.ReLU(),
            nn.Linear(hidden_size, hidden_size),
            nn.ReLU(),
            nn.Linear(hidden_size, n_warehouses)   # логиты для каждого действия
        )

    def forward(self, state):
        """
        state: словарь с ключами 'current', 'visited_mask', 'dist_vector'.
        Возвращает softmax-вероятности (маска посещённых зануляется).
        """
        current = state['current']                  # (batch,)
        visited_mask = state['visited_mask']         # (batch, n)
        dist_vector = state['dist_vector']           # (batch, n)
        # One-hot encoding текущего города
        current_onehot = torch.zeros(current.shape[0], self.n_warehouses, device=current.device)
        current_onehot.scatter_(1, current.unsqueeze(1), 1.0)
        # Конкатенация признаков
        x = torch.cat([current_onehot, visited_mask, dist_vector], dim=1)
        logits = self.network(x)
        # Запрещаем выбор уже посещённых городов
        logits[visited_mask.bool()] = -1e9
        return torch.softmax(logits, dim=-1)


