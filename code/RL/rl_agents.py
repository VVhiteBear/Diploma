class PrioritizedReplayBuffer:
    """
    Буфер воспроизведения с приоритезацией опыта.
    Хранит переходы и позволяет сэмплировать их с вероятностью,
    пропорциональной TD-ошибке. Ускоряет обучение, фокусируясь на важных примерах.
    """
    def __init__(self, capacity=10000, alpha=0.6):
        self.capacity = capacity
        self.alpha = alpha          # степень приоритезации (0 - нет, 1 - полная)
        self.buffer = []
        self.priorities = np.zeros(capacity, dtype=np.float32)
        self.position = 0
        self.size = 0

    def __len__(self):
        return self.size

    def add(self, experience, priority=None):
        """Добавление перехода с заданным или максимальным приоритетом."""
        if priority is None:
            priority = self.max_priority()
        if len(self.buffer) < self.capacity:
            self.buffer.append(experience)
        else:
            self.buffer[self.position] = experience
        self.priorities[self.position] = priority ** self.alpha
        self.position = (self.position + 1) % self.capacity
        self.size = min(self.size + 1, self.capacity)

    def sample(self, batch_size, beta=0.4):
        """Сэмплирование батча с учётом приоритетов и расчёт весов для коррекции смещения."""
        if self.size == 0:
            return [], [], []
        probs = self.priorities[:self.size] / self.priorities[:self.size].sum()
        indices = np.random.choice(self.size, min(batch_size, self.size), p=probs, replace=False)
        weights = (self.size * probs[indices]) ** (-beta)
        weights = weights / weights.max()
        batch = [self.buffer[i] for i in indices]
        return batch, indices, weights

    def update_priorities(self, indices, priorities):
        """Обновление приоритетов для указанных индексов."""
        for idx, priority in zip(indices, priorities):
            self.priorities[idx] = (priority + 1e-5) ** self.alpha

    def max_priority(self):
        """Возвращает текущий максимальный приоритет."""
        return self.priorities[:self.size].max() if self.size > 0 else 1.0

