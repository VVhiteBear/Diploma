class DuelingQNetwork(nn.Module):
    """
    Дуэльная Q-сеть: раздельная оценка ценности состояния V(s) и преимуществ A(s,a).
    Q(s,a) = V(s) + A(s,a) - mean(A(s,:)).
    Используется в ImprovedDQN и ImprovedDoubleDQN.
    """
    def __init__(self, n_warehouses, hidden_size=256):
        super().__init__()
        self.n_warehouses = n_warehouses
        self.encoder = nn.Sequential(
            nn.Linear(n_warehouses * 3, hidden_size),
            nn.ReLU(),
            nn.Linear(hidden_size, hidden_size),
            nn.ReLU()
        )
        self.value_stream = nn.Sequential(
            nn.Linear(hidden_size, hidden_size // 2),
            nn.ReLU(),
            nn.Linear(hidden_size // 2, 1)
        )
        self.advantage_stream = nn.Sequential(
            nn.Linear(hidden_size, hidden_size // 2),
            nn.ReLU(),
            nn.Linear(hidden_size // 2, n_warehouses)
        )

    def forward(self, state):
        current = state['current']
        visited_mask = state['visited_mask']
        dist_vector = state['dist_vector']
        current_onehot = torch.zeros(current.shape[0], self.n_warehouses, device=current.device)
        current_onehot.scatter_(1, current.unsqueeze(1), 1.0)
        x = torch.cat([current_onehot, visited_mask, dist_vector], dim=1)
        features = self.encoder(x)
        value = self.value_stream(features)
        advantage = self.advantage_stream(features)
        # Комбинируем с вычитанием среднего для устойчивости
        q_values = value + advantage - advantage.mean(dim=1, keepdim=True)
        q_values[visited_mask.bool()] = -1e9
        return q_values


class NoisyLinear(nn.Module):
    """
    Линейный слой с параметрическим шумом (Noisy Networks).
    Улучшает исследование за счёт шума в весах и смещениях.
    """
    def __init__(self, in_features, out_features, std_init=0.4):
        super().__init__()
        self.in_features = in_features
        self.out_features = out_features
        self.std_init = std_init

        self.weight_mu = nn.Parameter(torch.Tensor(out_features, in_features))
        self.weight_sigma = nn.Parameter(torch.Tensor(out_features, in_features))
        self.register_buffer('weight_epsilon', torch.Tensor(out_features, in_features))

        self.bias_mu = nn.Parameter(torch.Tensor(out_features))
        self.bias_sigma = nn.Parameter(torch.Tensor(out_features))
        self.register_buffer('bias_epsilon', torch.Tensor(out_features))

        self.reset_parameters()
        self.reset_noise()

    def reset_parameters(self):
        mu_range = 1 / np.sqrt(self.in_features)
        self.weight_mu.data.uniform_(-mu_range, mu_range)
        self.weight_sigma.data.fill_(self.std_init / np.sqrt(self.in_features))
        self.bias_mu.data.uniform_(-mu_range, mu_range)
        self.bias_sigma.data.fill_(self.std_init / np.sqrt(self.out_features))

    def reset_noise(self):
        epsilon_in = self._scale_noise(self.in_features)
        epsilon_out = self._scale_noise(self.out_features)
        self.weight_epsilon.copy_(epsilon_out.ger(epsilon_in))
        self.bias_epsilon.copy_(epsilon_out)

    def _scale_noise(self, size):
        x = torch.randn(size)
        return x.sign().mul(x.abs().sqrt())

    def forward(self, x):
        if self.training:
            weight = self.weight_mu + self.weight_sigma * self.weight_epsilon
            bias = self.bias_mu + self.bias_sigma * self.bias_epsilon
        else:
            weight = self.weight_mu
            bias = self.bias_mu
        return nn.functional.linear(x, weight, bias)


